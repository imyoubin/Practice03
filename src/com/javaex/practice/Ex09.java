package com.javaex.practice;

public class Ex09 {

	public static void main(String[] args) {
		
		for (int no = 1; no <= 10; no++) {
            for (int i = 0; i < 10; i++) {
                System.out.print((no + i) + "\t");
            }
            System.out.println(); // 줄 바꿈
			
		}	
			
	}
}
